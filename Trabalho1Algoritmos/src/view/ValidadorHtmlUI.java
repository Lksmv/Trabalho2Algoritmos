    private void jbSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSelecionarActionPerformed
        // TODO add your handling code here:
        JFileChooser fChooser = new JFileChooser();
        fChooser.setDialogTitle("Procurar Arquivos HTML");

        fChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);

        FileNameExtensionFilter filter = new FileNameExtensionFilter("Arquivos", "html");
        fChooser.setFileFilter(filter);
        int ret = fChooser.showOpenDialog(this);

        if (ret == JFileChooser.APPROVE_OPTION) {
            tfArquivo.setText(fChooser.getSelectedFile().toString());
        }
    }//GEN-LAST:event_jbSelecionarActionPerformed

    private void jbAnalisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAnalisarActionPerformed
        // TODO add your handling code here:
        try {
            ManipulaArquivo validar = new ManipulaArquivo();
            String extensaoArquivo = validar.getExtensao(tfArquivo.getText());
            if (extensaoArquivo.equalsIgnoreCase("html")) {
                ((DefaultTableModel) jtTag.getModel()).setRowCount(0);
                jtaOcorrencia.setText(validar.validarHtml(tfArquivo.getText()));
                ListaEncadeada<Tag> listaTag = validar.getLista();
                int i = 0;
                while (i < listaTag.obterComprimento()) {
                    Tag tag = listaTag.obterNo(i).getInfo();
                    adicionarValorTabela(tag.getTagNome(), tag.getTagQuantidade());
                    i++;
                }
            } else {
                if (tfArquivo.getText().trim().equals(""))
                    JOptionPane.showMessageDialog(null, "Selecione um arquivo!");
                else
                    JOptionPane.showMessageDialog(null, "Arquivo está com formato inválido!");
            }

        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(null, "Arquivo não encontrado!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Arquivo inválido!");
        }
    }//GEN-LAST:event_jbAnalisarActionPerformed

