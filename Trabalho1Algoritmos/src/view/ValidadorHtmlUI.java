    private void adicionarValorTabela(String tag, int quantidade) {

        Object[] linha = new Object[2];
        linha[0] = tag;
        linha[1] = quantidade;

        DefaultTableModel model;
        model = (DefaultTableModel) jtTag.getModel();
        model.addRow(linha);
    }

